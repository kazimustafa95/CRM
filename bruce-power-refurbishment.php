                     <h3 class=about_article-header>Description</h3>
                     <p class=about_article-text>The Bruce Power Refurbishment is a massive $13 billion project to refurbish two nuclear reactor
                        units at the Bruce Power site in Tiverton, Ontario. The refurbishment project involves the 
                        replacement of key reactor components, such as steam generators, calandria, and feeder tubes. 
                        The work on the first unit is set to begin in 2023, with the second unit scheduled for 2025.</p>
                        <br>
                        <br>

                     <p class=about_article-text>The project will add 30 to 35 years of operational life to each reactor and create and sustain 
                        22,000 direct and indirect jobs annually. The refurbishment of the reactors will also increase 
                        their output by approximately 30%, providing clean, reliable, and affordable energy to the 
                        province of Ontario.</p>
                        <br>
                        <br>
                  
                     <p class=about_article-text>Bruce Power is working with its contractors, which include AECON, Black & McDonald, and 
                        Framatome, to ensure that the project is completed on time and on budget, while maintaining the 
                        highest safety standards. The project is a key part of the company's commitment to providing 
                        low-cost, reliable, and clean energy to Ontario.</p>
                     
                        <br>
                        <br>
                  
                     <p class=about_article-text>The refurbishment of the Bruce Power site is also part of the company's long-term vision to play 
                        a leading role in the development of new nuclear technologies, such as small modular reactors. 
                        By investing in these technologies, Bruce Power aims to ensure that it continues to play a key 
                        role in meeting the energy needs of the province for decades to come.</p>

                        <br>
                        <br>
                  
                     <p class=about_article-text>The preparatory work for the project is already underway, and this includes the construction of 
                        new facilities to support the refurbishment work, such as a fuel channel manufacturing facility 
                        and a storage facility for reactor components.</p>

