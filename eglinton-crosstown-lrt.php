                     <h3 class=about_article-header>Description</h3>
                     <p class=about_article-text>The Eglinton Crosstown LRT is an 11.6-mile (19-kilometer) light rail transit line currently under
                        construction in Toronto, Ontario, Canada. The project is estimated to cost $11.996 billion and is 
                        scheduled to be completed in 2023. It will run along Eglinton Avenue between Mount Dennis 
                        (Weston Road) and Kennedy station, with a 10-kilometer underground portion between Keele 
                        Street and Laird Drive</p>
                        <br>
                        <br>

                     <p class=about_article-text>The Crosstown LRT will have 25 stations and stops and three subway stations. It will have a 
capacity of 15,000 passengers per hour per direction. The line will use light rail vehicles that are 
designed to be more accessible and comfortable than buses. The vehicles will be low-floor and 
fully accessible, with features such as on-board bicycle storage, Wi-Fi, and air conditioning.</p>

                    
                     <p class=about_article-text>Construction on the Eglinton Crosstown LRT began in 2011, and the project has faced several 
                        delays and cost overruns. However, it is now approaching completion, with testing and 
                        commissioning underway. The line is expected to provide faster, more reliable transit options for
                        residents in the areas it serves and help reduce traffic congestion in the Greater Toronto Area.</p>
                          <br>
                          <br>
                          <p class=about_article-text>One of the unique features of the Crosstown LRT is its innovative use of design and technology 
                           to minimize its impact on the surrounding communities. The line will be partially underground, 
                           and the stations will be designed to blend in with their surroundings, using materials such as 
                           wood and stone. The line will also feature green infrastructure, such as rain gardens, to manage 
                           stormwater runoff and improve local water quality.</p>
                          <br>
                          <br>
                          <p class=about_article-text>The Eglinton Crosstown LRT is expected to provide significant economic benefits to the region, 
                           including the creation of thousands of jobs during construction and increased access to 
                           employment, education, and other opportunities for residents. It is also expected to help reduce 
                           greenhouse gas emissions and support the transition to a more sustainable and livable city.</p>
