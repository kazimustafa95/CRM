                     <h3 class=about_article-header>Description</h3>
                     <p class=about_article-text>The Ontario Line is a planned 15.6-kilometer rapid transit line in Toronto, Ontario, 
                        Canada. The project, with a budget of $10.9 billion, is expected to be completed by 
                        2030. The line is designed to provide relief to the overcrowded Yonge-University 
                        subway line and connect several neighborhoods across the city.</p>
                     <br>
                     <br>
                     <p class=about_article-text>The Ontario Line will have 15 stations, with three transfer stations to connect with 
                        other transit lines in Toronto. The line will run from the Ontario Science Centre in the
                        northeast to Ontario Place on the waterfront in the southwest. Over half of the line 
                        will be underground, with the remainder running along elevated and at-grade rail 
                        corridors.</p>
                     <br>
                     <br>
                     <p class=about_article-text>The line is expected to have a ridership capacity of 400,000 passengers per day and
                        is designed to be fully accessible. The trains will be automated and driverless, with 
                        a frequency of approximately 90 seconds during peak periods.</p>
                     <br>
                     <br>
                     <p class=about_article-text>The project has faced some controversy, with concerns raised by some residents 
                        about the impact of construction on local neighborhoods and the environment. In 
                        response, the government has made efforts to address these concerns by 
                        implementing measures to mitigate noise and dust, preserving heritage buildings, 
                        and working with Indigenous communities to protect cultural heritage sites.</p>
                        <br>
                        <br>
                     <p class="about_article-text">
                        Construction of the Ontario Line is expected to begin in 2022, with completion of 
                        the first phase by 2027. The project is expected to create thousands of jobs in the 
                        construction industry and contribute to the economic growth of Toronto and the 
                        srrounding area.
                     </p>
                   
